/** Map of service ID → icon component. */
export const SERVICE_ICONS: Record<string, React.FC<IconProps>> = {
  '1password': OnePasswordIcon,
  telegram: TelegramIcon,
  github: GitHubIcon,
  gmail: GmailIcon,
  google_calendar: CalendarIcon,
  google_drive: GoogleDriveIcon,
  notion: NotionIcon,
  linear: LinearIcon,
  discord: DiscordIcon,
  slack: SlackIcon,
  typefully: TypefullyIcon,
  deepwiki: DeepWikiIcon,
};

/** Get the icon component for a service, falling back to DefaultServiceIcon. */
export function getServiceIcon(service: string): React.FC<IconProps> {
  return SERVICE_ICONS[service] ?? DefaultServiceIcon;
}
